#集合类型 存储无序并且不重复的数据
#元素只能是不可变数据类型 如：整数，浮点数，字符串，元组等
#可变数据类型：列表，字典，集合

s = {123,123,3.14,"abc","abc"}
print(s)
print(type(s))# <class 'set'>
'''
a - t 差集
a & t 交集
a ^ t 补集
a | t 并集
'''
a = {123,43,12,45,34,21}
t = {123,43,12,76,453,346}

#差集
print(a - t)

#交集
print(a & t)

#补集
print(a ^ t)

#并集
print(a | t)

