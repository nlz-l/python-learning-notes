import sys
input = sys.stdin.readline
import math
from collections import Counter

n, m = map(int, input().split())
num = []
for _ in range(n):
    num.append(list(map(int, input().split())))

djx1 = [[] for _ in range(n + m + 1)]
djx2 = [[] for _ in range(n + m + 1)]
for i in range(n):
    for j in range(m):
        djx1[i + j].append(num[i][j])
        djx2[i - j + m].append(num[i][j])

ans = 0
for i in range(1, n + m + 1):
    if len(djx1[i]) == 1:
        continue
    dic1 = Counter(djx1[i])
    for j in dic1:
        if dic1[j] > 1:
            ans += (math.factorial(dic1[j]) // math.factorial(dic1[j] - 2))

for i in range(1, n + m + 1):
    if len(djx2[i]) == 1:
        continue
    dic2 = Counter(djx2[i])
    for j in dic2:
        if dic2[j] > 1:
            ans += (math.factorial(dic2[j]) // math.factorial(dic2[j] - 2))

print(ans)


