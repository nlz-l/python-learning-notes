def ch_4(x):
    y = 0
    while x != 0:
        y = x % 4 + y
        x = x // 4
    return y

def ch_2(x):
    a = sum(list(map(int, str(bin(x))[2:])))
    return a

count = 0
for i in range(1, 2025):
    if ch_2(i) == ch_4(i):
        count += 1
print(count)


