str1 = ["lqb","lbq","qlb","qbl","blq","bql"]
str2 = input().strip()
count = 0
ans = 0
while count + 3 <= len(str2):
    if str2[count:count+3] in str1:
        count += 3
        ans += 1
    else:
        count += 1
print(ans)

