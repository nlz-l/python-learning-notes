#<模块字符串>.format(<参数列表>)
'''
模块字符串是一个由字符串和槽组成的字符串，用来控制字符串和变量的显示效果
槽用{ }表示，与format中的参数列表对应
'''

print("{}说：今天天气不错！".format("小明"))

print("{}说：今天天气{}".format("小明","不好"))

print("{0}说：今天天气{1}".format("小明","不好"))

print("{1}说：今天天气{0}".format("小明","不好"))

#print("{0}说：今天天气{1.机器学习概述}".format("小明")) #报错

print("{0}说：今天天气{0}".format("小明"))
