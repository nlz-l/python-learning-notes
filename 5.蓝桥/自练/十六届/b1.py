import os
import sys

cnt = 0
score = 2025
a = 5

def h_1(x: int) -> int:
    if x % 2 == 1:
        return 15
    else:
        return 2

def h_2(x: int) -> int:
    if x % 3 == 1:
        return 2
    elif x % 3 == 2:
        return 10
    else:
        return 7

while score > 0:
    cnt += 1
    b = h_1(cnt)
    c = h_2(cnt)
    score -= a + b + c

print(cnt)


