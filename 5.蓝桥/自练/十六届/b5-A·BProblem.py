L = int(input())

dp = [0] * (L + 1)

for i in range(1, L + 1):
    for j in range(i, L + 1, i):
        dp[j] += 1

s = [0] * (L + 1)
for i in range(1, L + 1):
    s[i] = s[i - 1] + dp[i]

result = 0
for a in range(1, L):
    result += dp[a] * s[L - a]

print(result)
"""
========================================
  第五题：A·B Problem 算法详解
========================================

【题目】
给定正整数 L，求满足以下条件的向量对 (Xₐ,Yₐ), (Xᵦ,Yᵦ) 的数量：
  1. Xₐ,Yₐ,Xᵦ,Yᵦ 均为正整数
  2. Xₐ·Xᵦ + Yₐ·Yᵦ ≤ L

【算法一：筛法统计约数个数】
dp[k] 表示 k 有多少个正整数约数对 (x,y) 满足 x*y=k

原理：对于每个 i（从1到L），它的倍数 j=i,2i,3i,... 都有约数 i
例如 L=6 时：
  i=1: j=1,2,3,4,5,6 → 每个dp[j] +=1
  i=2: j=2,4,6      → dp[2],dp[4],dp[6] 各+=1
  i=3: j=3,6        → dp[3],dp[6] 各+=1
  ...以此类推
结果: dp=[0,1,2,2,3,2,4]
含义: dp[6]=4 表示 6 有4种分解方式：(1,6),(2,3),(3,2),(6,1)


【算法二：前缀和】
s[k] = dp[1]+dp[2]+...+dp[k]，表示满足 x*y≤k 的总对数

原理：累加 dp 数组，快速查询"不超过k的总方式数"
例如：
  s[1]=1   → x*y≤1 的对：(1,1)
  s[2]=3   → x*y≤2 的对：(1,1),(1,2),(2,1)
  s[3]=5   → x*y≤3 的对：再加(1,3),(3,1)
  ...


【算法三：乘法原理（组合计数）】

令 a=Xₐ·Xᵦ, b=Yₐ·Yᵦ，则题目变为：a+b≤L, a≥1, b≥1

对于每个固定的a：
  - 选(Xₐ,Xᵦ)使乘积=a → 有 dp[a] 种方式
  - 选(Yₐ,Yᵦ)使乘积≤L-a → 有 s[L-a] 种方式
  - 根据乘法原理：共 dp[a] × s[L-a] 种

遍历所有可能的a，累加即得最终答案


【三大算法关系图】

  题目：Xₐ·Xᵦ + Yₐ·Yᵦ ≤ L
       │
       ├── 分解问题
       │   ├── a = Xₐ·Xᵦ
       │   └── b = Yₐ·Yᵦ
       │
       ├── 【筛法】→ dp数组（每个数的约数个数）
       │
       ├── 【前缀和】→ s数组（不超过某数的总方式）
       │
       └── 【乘法原理】→ result = Σ dp[a]×s[L-a]


【一句话总结】
筛法算出每个数的分解方式 → 前缀和快速查询累计值 → 乘法原理组合得到答案
"""